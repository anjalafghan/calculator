package calculator;


public class calculations implements Operators {

    public float add(float first, float second){
        return (first+second);

    }
    public float sub(float first, float b){
        return (first-b);

    }
    public float div(float first, float second){
        return (first/second);

    }
    public float mul(float first, float second){
        return (first*second);

    }


}
